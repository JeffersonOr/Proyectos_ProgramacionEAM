package modelo;

/**
 *
 * @author briantafur
 */
public class Libro {
    
    private int isbn;
    private String nombre;
    private String nombreAutor;

    public Libro(int isbn, String nombre, String nombreAutor) {
        this.isbn = isbn;
        this.nombre = nombre;
        this.nombreAutor = nombreAutor;
    }

    public int getIsbn() {
        return isbn;
    }

    public void setIsbn(int isbn) {
        this.isbn = isbn;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNombreAutor() {
        return nombreAutor;
    }

    public void setNombreAutor(String nombreAutor) {
        this.nombreAutor = nombreAutor;
    }
    
    
    
}
